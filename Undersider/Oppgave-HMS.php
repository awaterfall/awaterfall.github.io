        HMS står for Helse Miljø og Sikkerhet, og er et regelverk som er til for å passe på sikkerheten til arbeidstakere når de er på jobb. 
        HMS sørger for at om man blir skadet på jobb må man få erstatning fra arbeidsgiver på grunn av manglende sikkerhetsutstyr. 
        Arbeidsgiver skal sørge for at det blir gjort brannøvelser jevnlig og at det er nødvendig utstyr, som for eksempel brannslukningsapparat og førstehjelpsskrin, tilgjengelig på arbeidsplassen. 
        Arbeidsgiver må sørge for variasjon i arbeidet for arbeidstakeren slik at arbeidstaker ikke blir låst til én oppgave. 
        Arbeidsgiveren er ansvarlig for at arbeidsplassen er tilpasset oppgavene folk skal utføre, på byggeplasser kan dette være riktig utstyr og sikkerhet og på kontorer kan dette være ergonomisk utstyr og briller som stopper blått lys. 
        Arbeidsgiver er ansvarlig for at arbeidstaker blir sendt på kurs om det er nødvendig. 
        Arbeidstaker skal få ha en mening om hva de liker/ønsker å jobbe med. <br>
